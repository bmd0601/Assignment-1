#Write a Python program that accepts a word from the user and reverse it.

# The medthod which i used to get the expected output is string slicing

# input :
name = "Edyoda"
# The [::-1] slicing is used to reverse the string. 
# This creates a new string that is a copy of the original string, but with the characters in reverse order.
print(name[::-1])
# by using the slicing method we will get the expected output
#----------------------------------------------------------------------
# Output:
# adoydE
